#esta linea definira una lista con algunos elementos
mi_lista = [10, 20, 30, 40, 50]

#esta linea iniciamos el indice en 0, ya que vamos a recorrer la lista desde el primer elemento
indice = 0

#con esta linea usamos un bucle while para recorrer la lista
while indice < len(mi_lista):
    #con esta linea imprimimos el elemento actual de la lista
    print(mi_lista[indice])
    
    #con esta linea aumentamos el indice para pasar al siguiente elemento
    indice += 1
